int exgcd(int a,int b,int &x,int &y){
	if(b==0){
		x=1,y=0;
		return a;
	}
	int d=exgcd(b,a%b,y,x);
	y-=a/b*x;
	return d;
} 
//S=1/2*|x*v-y*u|
pii get(int x,int y,int v,int u){
	int d=exgcd(x,y,v,u);
	u=-u;
    return {u,v};
}

int t;

void solve(){
    int a,b,c,d;
    cin>>a>>b>>c>>d;
    int y=d-b,x=c-a;
    pii ans=get(x,y,0,0);
    cout<<ans.fi+a<<" "<<ans.se+b<<'\n';
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
	cin>>t;
    while(t--){
        solve();
    }
	return 0;
}